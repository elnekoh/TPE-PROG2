package TPE;

public class Carta {
	private double altura;
	private double peso;
	private double fuerza;
	private int peleasGanadas;
	private double velocidad;
	
	public Carta(double altura, double peso, double fuerza, int peleasGanadas, double velocidad) {
		this.altura = altura;
		this.peso= peso;
		this.fuerza = fuerza;
		this.peleasGanadas = peleasGanadas;
		this.velocidad = velocidad;
	}
	
}
